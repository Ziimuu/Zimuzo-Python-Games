import turtle

zimu = turtle.Turtle()

zimu.color("blue", "cyan")

zimu.begin_fill()

zimu.forward(100)
zimu.left(90)
zimu.forward(100)
zimu.left(90)
zimu.forward(100)
zimu.left(90)
zimu.forward(100)

zimu.penup()
zimu.forward(100)
zimu.pendown()

zimu.forward(100)
zimu.left(90)
zimu.forward(100)
zimu.left(90)
zimu.forward(100)
zimu.left(90)
zimu.forward(100)

zimu.end_fill()


turtle.done()